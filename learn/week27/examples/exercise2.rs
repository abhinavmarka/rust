struct rect {
    width: f32,
    height: f32,
}

impl rect {
    fn area(&self) -> f32 {
        self.width * self.height
    }
}

fn main() {
    let r = rect{
        width: 10.0, 
        height: 10.0
};

println!("{}, {}", r.width, r.height);
println!("area of rectangle is {}", r.area());

}





























































































































































































































































































































































































































































































































































































































































